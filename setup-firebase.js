/**
 * Firebase Config Setup Script
 * Ishlatish: node setup-firebase.js
 * 
 * Bu script sizdan Firebase config ma'lumotlarini so'raydi
 * va firebase-config.js + www/firebase-config.js fayllarini yangilaydi,
 * keyin APK ni qayta build qiladi.
 */

const fs = require('fs');
const path = require('path');
const readline = require('readline');

const rl = readline.createInterface({
    input: process.stdin,
    output: process.stdout
});

function ask(q) {
    return new Promise(resolve => rl.question(q, resolve));
}

async function main() {
    console.log('\n========================================');
    console.log('  🔥 Firebase Config Sozlash');
    console.log('========================================\n');
    console.log('Firebase Console dan olingan qiymatlarni kiriting:\n');

    const apiKey = await ask('  apiKey: ');
    const authDomain = await ask('  authDomain: ');
    const projectId = await ask('  projectId: ');
    const storageBucket = await ask('  storageBucket: ');
    const messagingSenderId = await ask('  messagingSenderId: ');
    const appId = await ask('  appId: ');

    if (!apiKey || !projectId) {
        console.log('\n❌ apiKey va projectId majburiy!');
        rl.close();
        process.exit(1);
    }

    const configContent = `/**
 * Firebase Configuration — Labo Oltiariq Taxi
 * Auto-generated by setup-firebase.js
 */

const firebaseConfig = {
    apiKey:            "${apiKey}",
    authDomain:        "${authDomain}",
    projectId:         "${projectId}",
    storageBucket:     "${storageBucket}",
    messagingSenderId: "${messagingSenderId}",
    appId:             "${appId}"
};

// Firebase ni ishga tushirish
firebase.initializeApp(firebaseConfig);
const db = firebase.firestore();

// Offline qo'llab-quvvatlash
db.enablePersistence({ synchronizeTabs: true })
    .catch(err => {
        if (err.code === 'failed-precondition') {
            console.warn('Offline persistence: bir nechta tab ochiq');
        } else if (err.code === 'unimplemented') {
            console.warn('Offline persistence bu brauzerda ishlamaydi');
        }
    });

/**
 * Firestore tuzilmasi:
 *
 * settings/global        → { minPrice, perKm, perWaitMin }
 * drivers/{id}           → { name, carNumber, phone, username, password, note, active, createdAt }
 * trips/{id}             → { driverId, driverName, date, price, distance, waitMinutes }
 */
`;

    // Fayllarni yozish
    const rootFile = path.join(__dirname, 'firebase-config.js');
    const wwwFile = path.join(__dirname, 'www', 'firebase-config.js');

    fs.writeFileSync(rootFile, configContent, 'utf8');
    console.log('\n  ✅ firebase-config.js yangilandi');

    if (fs.existsSync(path.join(__dirname, 'www'))) {
        fs.writeFileSync(wwwFile, configContent, 'utf8');
        console.log('  ✅ www/firebase-config.js yangilandi');
    }

    console.log('\n========================================');
    console.log('  ✅ Firebase Config tayyor!');
    console.log('========================================');
    console.log('\nKeyingi qadamlar:');
    console.log('  1. Admin panelni oching: admin.html');
    console.log('  2. Login: admin / Parol: labo2024');
    console.log('  3. Haydovchi qo\'shing va narxlarni o\'rnating');
    console.log('  4. APK qayta build: build-apk.bat\n');

    rl.close();
}

main().catch(err => { console.error(err); rl.close(); });
